export type IconName = keyof typeof ICONS;

export type IconOptions = JSX.HTMLAttributes<HTMLSpanElement> & {
	class?: string;
	variant?: Variant;
	tooltip?: string;
} & ({name: IconName; url?: string} | {name: undefined; url: string});

export function Icon({name, url, class: className, variant, tooltip}: IconOptions) {
	let classNames = `Icon`;
	if (name) classNames += ` -n-${name}`;
	if (className) classNames += ` ${className}`;
	if (variant) classNames += ` -variant-${variant}`;

	return (
		<span class={classNames} title={tooltip} aria-hidden="true">
			{name ? (
				ICONS[name!]
			) : (
				<svg focusable="false">
					<use href={url} />
				</svg>
			)}
		</span>
	);
}

